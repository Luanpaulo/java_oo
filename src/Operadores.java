
public class Operadores {
	public void soma() {
		int x = 3;
		int y = 6;
		int result = x + y;
		System.out.println(result);
	}
	public void sub() {
		int x = 5;
		int y = 4;
		int result = x - y;
		System.out.println(result);
	}
	public void mult() {
		int x = 2;
		int y = 4;
		int result = x * y;
		System.out.println(result);
	}
	public void div() {
		int x = 4;
		int y = 2;
		int result = x / y;
		System.out.println(result);
	}
	// Soma, Subtração, Multiplicação, Divisão
	
}
